// navigator / localStorage are guarded so this module is safe to import outside a
// browser DOM (the renderer also runs under `vite dev` and is pulled into node
// unit tests via errors.ts → i18n) — same defensive pattern as PlayView.
function resolveAuto(): Lang {
  const sys = (typeof navigator !== "undefined" ? navigator.language : "en").toLowerCase();
  return sys.startsWith("zh") ? "zh" : "en";
}

export function getLangPref(): LangPref {
  if (typeof localStorage === "undefined") return "auto";
  const saved = localStorage.getItem(LANG_KEY);
  if (saved === "en" || saved === "zh" || saved === "auto") return saved;
  return "auto";
}

export function setLangPref(pref: LangPref): void {
  if (typeof localStorage !== "undefined") localStorage.setItem(LANG_KEY, pref);
  void i18next.changeLanguage(pref === "auto" ? resolveAuto() : pref);
}

const initialPref = getLangPref();

void i18next.use(initReactI18next).init({
  resources: { en, zh },
  lng: initialPref === "auto" ? resolveAuto() : initialPref,
  fallbackLng: "en",
  interpolation: { escapeValue: false },
});

export default i18next;
