#!/usr/bin/env node
// Reads src/store/schema.sql and emits src/store/schema.generated.ts
// containing a SCHEMA_SQL_V1 string constant for sqlite.ts to exec().
// Idempotent: running twice yields byte-identical output.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const src = path.resolve(here, "..", "src", "store", "schema.sql");
const out = path.resolve(here, "..", "src", "store", "schema.generated.ts");

const sql = fs.readFileSync(src, "utf8");
const body = `// AUTO-GENERATED by scripts/bundle-schema.mjs from schema.sql.
// DO NOT EDIT. DO NOT COMMIT (see runtime/.gitignore).
// Regenerate via \`npm run gen:schema\` (chained into check-types + test).

export const SCHEMA_SQL_V1 = ${JSON.stringify(sql)};
`;
fs.writeFileSync(out, body);
console.log(`[bundle-schema] wrote ${path.relative(process.cwd(), out)} (${sql.length} chars)`);
